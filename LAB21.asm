.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
;DISPLAY ?
	MOV AH, 2
	MOV DL,'?'
	INT 21H
;Input 2 Character
	MOV AH, 1
	INT 21H
	MOV CL,AL
	INT 21H
	MOV BL,AL
;COMPAIR Variable
	CMP CL, BL
	JA  BEHIND
	JMP END_IF
;BEHIND
BEHIND:
	MOV AL,CL
	MOV CL,BL
	MOV BL,AL
END_IF:
;Go NEW LINE
	MOV AH, 2
	MOV DL, 0DH
	INT 21H
	MOV DL, 0AH
	INT 21H
;DISPLAY
	MOV DL,CL
	INT 21H
	MOV DL,BL
	INT 21H
; show result to DOS
	MOV	AH, 4CH		; DOS exit function
	INT	21H		; exit to DOS
MAIN	ENDP
	END MAIN
	